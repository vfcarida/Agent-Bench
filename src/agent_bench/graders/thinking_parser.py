"""Thinking parser middleware for SwiReasoning evaluation.

Handles <think>...</think> blocks generated by Athena's Phase 1d
(SwiReasoning). Provides:
  1. A parser that separates thinking content from the final answer.
  2. A grader middleware that strips thinking before passing to graders.
"""

from __future__ import annotations

import re
from dataclasses import dataclass, field
from typing import Any, Callable

from agent_bench.graders.state_grader import GradeResult

# Pattern to match <think>...</think> blocks (including multiline)
_THINK_PATTERN = re.compile(r"<think>(.*?)</think>", re.DOTALL)


@dataclass
class ParsedResponse:
    """Result of parsing a response with <think> blocks.

    Attributes:
        raw_response: The original, unmodified response string.
        clean_response: Response with all <think> blocks removed and whitespace normalized.
        thinking_blocks: List of extracted thinking content strings.
        thinking_token_count: Approximate token count of all thinking content.
        answer_token_count: Approximate token count of the clean answer.
    """

    raw_response: str
    clean_response: str
    thinking_blocks: list[str] = field(default_factory=list)
    thinking_token_count: int = 0
    answer_token_count: int = 0

    @property
    def has_thinking(self) -> bool:
        """Whether the response contained any <think> blocks."""
        return len(self.thinking_blocks) > 0

    @property
    def total_token_count(self) -> int:
        """Total approximate tokens (thinking + answer)."""
        return self.thinking_token_count + self.answer_token_count

    @property
    def thinking_ratio(self) -> float:
        """Ratio of thinking tokens to total tokens."""
        total = self.total_token_count
        if total == 0:
            return 0.0
        return self.thinking_token_count / total


def _estimate_tokens(text: str) -> int:
    """Rough token count estimate (~4 chars per token for English).

    This is used for metrics when a real tokenizer is not available.
    For more accurate counts, use the model's tokenizer directly.
    """
    if not text:
        return 0
    return max(1, len(text.split()))


def parse_thinking_response(response: str) -> ParsedResponse:
    """Parse a response to separate <think> blocks from the answer.

    Handles:
    - Single <think>...</think> blocks
    - Multiple <think> blocks scattered through the response
    - Nested content within <think> blocks
    - Responses with no <think> blocks (passthrough)

    Args:
        response: The raw model response string.

    Returns:
        ParsedResponse with separated thinking and clean answer.
    """
    if not response:
        return ParsedResponse(
            raw_response="",
            clean_response="",
        )

    # Extract all <think> block contents
    thinking_blocks = _THINK_PATTERN.findall(response)

    # Remove <think> blocks from the response
    clean_response = _THINK_PATTERN.sub("", response).strip()

    # Normalize whitespace in clean response
    clean_response = re.sub(r"\n{3,}", "\n\n", clean_response)

    # Estimate token counts
    thinking_text = " ".join(thinking_blocks)
    thinking_tokens = _estimate_tokens(thinking_text)
    answer_tokens = _estimate_tokens(clean_response)

    return ParsedResponse(
        raw_response=response,
        clean_response=clean_response,
        thinking_blocks=thinking_blocks,
        thinking_token_count=thinking_tokens,
        answer_token_count=answer_tokens,
    )


class ThinkingAwareGraderMiddleware:
    """Wraps any grader to strip <think> blocks before scoring.

    This middleware ensures that graders (StateGrader, RubricGrader,
    ToolCallGrader, CompositeGrader) evaluate only the final answer,
    not the reasoning chain. The thinking content is preserved in the
    grade result details for metric computation.

    Usage:
        grader = CompositeGrader()
        aware_grader = ThinkingAwareGraderMiddleware(grader)
        result = aware_grader.grade(case, actual_response="<think>...</think> answer")
    """

    def __init__(self, inner_grader: Any) -> None:
        """Initialize with any grader that has a .grade() method.

        Args:
            inner_grader: A grader instance (CompositeGrader, RubricGrader, etc.)
        """
        self._inner = inner_grader

    def grade(
        self,
        case: dict[str, Any],
        actual_state: dict[str, Any] | None = None,
        actual_tool_calls: list[dict[str, Any]] | None = None,
        actual_response: str = "",
        judge_fn: Callable[..., Any] | None = None,
    ) -> GradeResult:
        """Grade after stripping <think> blocks from the response.

        The parsed thinking analysis is attached to the grade result
        details under the 'thinking_analysis' key.
        """
        parsed = parse_thinking_response(actual_response)

        # Delegate to inner grader with clean response
        result = self._inner.grade(
            case,
            actual_state=actual_state,
            actual_tool_calls=actual_tool_calls,
            actual_response=parsed.clean_response,
            judge_fn=judge_fn,
        )

        # Attach thinking analysis to result details
        if parsed.has_thinking:
            result.details["thinking_analysis"] = {
                "thinking_blocks_count": len(parsed.thinking_blocks),
                "thinking_token_count": parsed.thinking_token_count,
                "answer_token_count": parsed.answer_token_count,
                "thinking_ratio": parsed.thinking_ratio,
                "had_thinking": True,
            }
        else:
            result.details["thinking_analysis"] = {
                "had_thinking": False,
            }

        return result
